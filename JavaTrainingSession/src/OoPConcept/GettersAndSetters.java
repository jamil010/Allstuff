package OoPConcept;

public class GettersAndSetters {
	private int i;
	private int j;

	public static void main(String[] args) {

		GettersAndSetters ob = new GettersAndSetters();
		
		ob.setI(6);
		
		
		
	}

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public int getJ() {
		return j;
	}

	public void setJ(int j) {
		this.j = j;
	}

}
